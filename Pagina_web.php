                    <h2>Definición de salud mental</h2>
                    <h3>La salud mental es un estado de bienestar mental que permite a las personas hacer frente 
                        a los momentos de estrés de la vida, desarrollar todas sus habilidades, poder aprender y 
                        trabajar adecuadamente y contribuir a la mejora de su comunidad. Es parte fundamental de 
                        la salud y el bienestar que sustenta nuestras capacidades individuales y colectivas para 
                        tomar decisiones, establecer relaciones y dar forma al mundo en el que vivimos. La salud 
                        mental es, además, un derecho humano fundamental. Y un elemento esencial para el desarrollo 
                        personal, comunitario y socioeconómico.<br></br>
                        La salud mental es más que la mera ausencia de trastornos mentales. Se da en un proceso 
                        complejo, que cada persona experimenta de una manera diferente, con diversos grados de 
                        dificultad y angustia y resultados sociales y clínicos que pueden ser muy diferentes.<br></br>
                        Las afecciones de salud mental comprenden trastornos mentales y discapacidades psicosociales, 
                        así como otros estados mentales asociados a un alto grado de angustia, discapacidad funcional 
                        o riesgo de conducta autolesiva. Las personas que las padecen son más propensas a experimentar 
                        niveles más bajos de bienestar mental, aunque no siempre es necesariamente así.<br></br>
                        </h3>
                        <br></br>

                    <h2>INSOMNIO</h2>
                    <h3>El insomnio es un desorden tremendamente común. Casi todos lo padecemos por lo menos de vez 
                        en cuando. Muchas cosas pueden causar una noche desvelada. De hecho, se ha demostrado que 
                        una de cada cinco personas padece insomnio en todo el mundo.<br></br>
                        Pero, ¿qué es el insomnio?  Se considera insomnio cuando existe una incapacidad para dormir 
                        o se duerme poco durante varios días consecutivos. Aquí no se incluye el hecho que un día, 
                        de forma aislada, una persona no haya podido dormir. Básicamente el insomnio es la percepción 
                        que tiene el individuo de un sueño inadecuado o de pobre calidad de forma repetida. Junto a 
                        esta sintomatología la persona padece diversas manifestaciones físicas y psíquicas asociadas, 
                        como dolor de cabeza, falta de concentración, irritabilidad, etc., que le impide realizar su 
                        día con normalidad, como acudir al trabajo, escuela o realizar sus tareas de forma continuada 
                        en el tiempo.

                        </h3>
                        <br></br>

                    <h2>DEPRESIÓN</h2>
                    <h3>
                    La depresión es una grave enfermedad mental que se caracteriza por una pérdida del ánimo, llanto 
                    incontrolado, ideación de ruina, pérdida de interés y, en ocasiones, pensamientos suicidas. Estos 
                    síntomas variados acompañan a los estados depresivos.<br></br>
                    En términos de prevalencia, la depresión afecta a todos, pero en mayor medida a las mujeres (con 
                    una proporción de 3 mujeres por cada hombre). A pesar de esto, la mayor incidencia de suicidios 
                    consumados corresponde a los varones. Además, se ha demostrado que las mujeres reaccionan de forma 
                    más ansiosa que los hombres frente a situaciones conflictivas de la vida.<br></br>
                    Cuando sufrimos depresión, experimentamos tristeza profunda. Cualquier pequeño esfuerzo nos agota, 
                    perdemos el sentido del humor y las ganas de hacer lo que normalmente nos entusiasma. Sin embargo, 
                    en la depresión grave, este estado se instaura de manera permanente, como si nos hundiéramos en un 
                    pozo sin fondo del cual parece imposible salir.<br></br>
                    Las personas deprimidas a menudo sienten aversión hacia sí mismas, se perciben como inútiles y 
                    culpables de sus insuficiencias. Pueden experimentar ataques de llanto, pérdida de peso, insomnio 
                    y desinterés por las personas cercanas. Los deseos suicidas pueden surgir y, a medida que se vuelven 
                    más serios, las ideas esporádicas se convierten en deseos con planes concretos. La depresión grave es 
                    uno de los trastornos psicológicos más debilitadores y dolorosos .
                    </h3>
                        <br></br>

                    <h2>TRANSTORNO POR DÉFICIT DE ATENCIÓN (TDAH)</h2>
                    <h3>
                    El Déficit de Atención con Hiperactividad (TDAH) es un «síndrome», es decir, un grupo de síntomas 
                    que típicamente ocurren juntos. Los síntomas principales del TDAH son la impulsividad, falta de 
                    atención e hiperactividad. Las estimaciones de la prevalencia del TDAH en los niños en edad escolar 
                    varían, pero la estimación media se estima que es del 2% entre los niños y niñas en edad escolar.<br></br>
                    Diversos estudios sobre familias y gemelos, demuestran que los factores genéticos son muy importantes 
                    en el TDAH, pero los factores ambientales también juegan un papel crucial, ya que la heredabilidad es 
                    inferior al 100%. Los factores ambientales, incluyendo el nacimiento prematuro, lesión en la cabeza, 
                    el síndrome de alcoholismo fetal, la exposición prenatal a drogas de abuso, como la cocaína, la 
                    toxicidad del plomo, el tabaquismo materno prenatal, y las anormalidades endocrinas raras pueden 
                    todos hacer que el síndrome de TDAH.
                    </h3>
